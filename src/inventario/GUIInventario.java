    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
       int  seleccionarFila = tabla.getSelectedRow();
       if (seleccionarFila >= 0) {
                    cCodigo.setText((String) tabla.getValueAt(seleccionarFila, 0));
                    cNombre.setText((String) tabla.getValueAt(seleccionarFila, 1));
                    cCantidadStock.setText(tabla.getValueAt(seleccionarFila, 2).toString());
                    cPrecio.setText(tabla.getValueAt(seleccionarFila, 3).toString());
                    cCategoria.setText((String) tabla.getValueAt(seleccionarFila, 4));
                    cCantidadR.setText(tabla.getValueAt(seleccionarFila, 5).toString());
                    filas=seleccionarFila;
                }
       
    }//GEN-LAST:event_tablaMouseClicked

