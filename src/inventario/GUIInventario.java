    public void AgregarTabla(){
         String codigo = cCodigo.getText();
         String nombre = cNombre.getText();
         
         String cantidadText = cCantidadStock.getText();
         int cantidad = cantidadText.isEmpty() ? 0 : Integer.parseInt(cantidadText);
         
         String precioText = cPrecio.getText();
         double precio = precioText.isEmpty() ? 0.0 : Double.parseDouble(precioText);
         
         String categoria = cCategoria.getText();
         
         String cantidadRecompraText = cCantidadR.getText();
         int cantidadRe = cantidadRecompraText.isEmpty() ? 0 : Integer.parseInt(cantidadRecompraText);
         
         this.validarCodigosIngresados();
         
         if(codigo.isEmpty() || nombre.isEmpty() ||  cantidad<=0 || precio<=0 || categoria.isEmpty() || cantidadRe<=0){
             
             JOptionPane.showMessageDialog(null, "Deje llenar los campos");
             }else{
        
            String []info  = new String[6];
            info[0] = cCodigo.getText();
            info[1] = cNombre.getText();
            info[2] = cCantidadStock.getText();
            info[3] = cPrecio.getText();
            info[4] = cCategoria.getText();
            info[5] = cCantidadR.getText();
            modelo.addRow(info);
 
             cCodigo.setText("");
             cNombre.setText("");
             cCantidadStock.setText("");
             cPrecio.setText("");
             cCategoria.setText("");
             cCantidadR.setText("");
         }
    }
    public void validarCodigosIngresados(){
        for(int i=0; i<tabla.getRowCount();i++){
            if(tabla.getValueAt(i, 0).equals(cCodigo.getText())){
                JOptionPane.showMessageDialog(null, "Error, El Codigo Ya Existe");
                modelo.removeRow(i);
        }else{
                AgregarTabla();
            }
    }}
    
    
    

    

         
    
    

